<p>Volleyball er en ballidrett. Det hele går ut på å slå dine motstandere. Man har et nett mellom 2 lag, så er det om å gjøre å holde ballen i lufta og få
 den over på motstanderen sin side. Hvis ballen går i bakken på egen side innenfor banen får motstanderen poeng. Førstemann til 21 poeng vinner, men man må vinne 
 med to poeng. Volleyball er en sport som man kan spille ute og inne. De fleste foretrekker ute i sand hvis været tillater det.</p>

<p>Når du spiller volleyball så handler det om å bruke hendene, så for å klare å spille volleyball bør du ha god ballkontroll med hendene. Det er også viktig å ha spenst og klare å samarbeide med de på laget ditt. 
Når du spiller volleyball så får du god spensttrening og du beveger deg masse. Hvis du er glad i samarbeid og harde og utfordrende kamper, så er volleyball noe for deg!</p>

