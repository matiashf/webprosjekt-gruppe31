  <blockquote cite="http://helsedirektoratet.no/folkehelse/ernering/kostholdsrad/Sider/default.aspx">

    <p>Spis minst fem porsjoner grønnsaker, frukt og bær hver dag. La
    grønnsaker og frukt inngå i alle dagens måltider. Gå på
    oppdagelsesferd i frukt- og grøntavdelingen. Smak og
    eksperimenter. Velg ulike farger.</p>

    <ul>

      <li>En porsjon tilsvarer 100 gram, og kan for eksempel være en liten
      bolle med salat, en gulrot eller en middels stor frukt.</li>

      <li>Du kan bruke friske, hermetiske, frosne, og varmebehandlede
      grønnsaker, frukt og bær. Mulighetene er mange. Halvparten av «fem
      om dagen» bør være grønnsaker.</li>

      <li>Varier mellom ulike typer grønnsaker og frukt. Bruk gjerne
      tomater, løk,purre og hvitløk. </li>

      <li>Poteter er ikke inkludert i «fem om dagen», men hører med i et
      variert kosthold. Poteter inneholder mer kostfiber, vitaminer og
      mineraler enn vanlig ris og pasta. Du bør helst velge kokte eller
      bakte poteter. </li>

      <li>Belgvekster som bønner og linser, frø, krydder og urter inngår
      heller ikke i «fem om dagen». Disse har ofte et høyt innhold av
      næringsstoffer og hører med i et variert kosthold.</li>

      <li>Spis gjerne en liten håndfull usaltede nøtter om
      dagen.</li>
    </ul>

    <p><a href="http://helsenorge.no/Helseogsunnhet/Sider/Spis-fem-om-dagen.aspx">Mer
