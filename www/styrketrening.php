<p>Styrketrening er trening med mål å øke muskelmassen og få en sterkere og mer markert kropp.</p>

<p>Styrketrening egner seg for alle som vil bli sterkere. Men folk som ikke er så kjent med trening
burde ta det litt med ro og eventuelt spørre en personlig trener eller rådføre seg med noen som har peil
før de begynner å trene for hardt. I styrketrening er det essensielt med riktig løfteteknikk
og feil løfteteknikk kan gi belastningsskader. Det er viktig å starte smått og kroppen bør helst
bli varm før man begynner å løfte. </p>

<p>Man kan trene styrke med egen kroppsvekt, manualer eller spesielle apparater for å trene styrke.
Man kan trene hjemme, ute i det fri eller på treningsstudio der de ofte har flere apparater for kroppens
ulike muskelgrupper.</p>

<p>Er du interessert i å begynne å trene kan du for eksempel melde deg inn i 
