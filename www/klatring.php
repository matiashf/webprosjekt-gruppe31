<p>Klatring går ut på å klatre i høyden, ofte på en klatrevegg. Av utstyr trengs det gode sko til å klatre med og sikkerhetsseler rundt kroppen
i tilfelle det blir fall.
Det er en idrett for deg som har lyst til å utfordre deg seg på flere plan. Her kommer du opp i høyden og får virkelig testet dine egne grenser høyde messig, men også med tanke på styrke og psyken du innehar i kroppen din. 
Klatring er en veldig fysisk idrett som gjør at du får trent veldig mange muskelgrupper og eksplosiviteten i kroppen din </p>

<p>Klatring egner seg for personer som har lyst til å trene på en litt utradisjonell måte, samtidig som man får trent godt og effektivt.
Klatring er en individuell sport, samtidig som man er sosial med andre som klatrer med deg og det er ofte flere personer som er tilstede under klatreøkten.</p>

