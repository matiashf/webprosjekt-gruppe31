<p>Badminton er en sport som er for 2-4 spillere. Det handler om å få fjærballen over nettet, sånn at man ikke taper. Hvis fjærballen lander på gulvet på din side har du tapt. Denne sporten egner seg for de fleste, det eneste som du bør "ha" er gode reflekser. Fjærballen går fort fram og tilbake så gode reflekser kan være
en god fordel. </p>

<p>Badminton er vel ikke en av de enkleste idrettene å begynne med. Det krever litt innsats fra deg selv, mest fordi det er vanskelig å spille det ute. Fjærballen er veldig lett, så den blir ofte tatt av vinden. Derfor er badminton en innendørs idrett, det går an å spille ute men det kan være litt frustrerende. Badminton er en veldig morsom sport, så det anbefales på det sterkeste!</p>

