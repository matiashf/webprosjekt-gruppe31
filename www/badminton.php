<p>Badminton er en sport som spilles mellom 2-4 spillere. Utstyret er racketer og en fjærball. 
<br>Det går ut på å få fjærballen over nettet, sånn at man ikke taper. Hvis fjærballen lander på gulvet på din side har du tapt. 
<br>Denne sporten egner seg for de fleste, det eneste som du bør "ha" er gode reflekser. Fjærballen går fort fram og tilbake så gode reflekser kan være en stor fordel. Hvis du ikke innehar gode nok reflekser fra før blir de trent i løpet av spillet.
</p>

<p>Badminton er vel ikke en av de enkleste idrettene å begynne med. Det krever litt innsats fra deg selv, mest fordi det er vanskelig å spille det ute. 
<br>Fjærballen er veldig lett, så den kan ofte bli "tatt" av vinden. Derfor er badminton mest egnet til å være en innendørs idrett, men det går fint an å spille det ute også men kan da oppleves litt frustrerende for noen. 
<br>Badminton er en veldig morsom sport, så det anbefales på det sterkeste!</p>

